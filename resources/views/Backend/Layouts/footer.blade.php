<script>

    $(document).ready(function(){

        $('#country_id').on('change',function(){

            var country_id = $(this).val();

            // alert(country_id);

            if(country_id == "")
            {
                alert('Pleas Select A Country');
            }
            else
            {
                $.ajax({
                    headers : {
                    'X-CSRF-TOKEN' : "{{ csrf_token() }}"
                    },
                    url : '{{url('getDivision')}}',

                    type : 'POST',

                    data : {
                        id:country_id,
                    },
                    success : function(data)
                    {
                        $('#division').html(data);

                    }
                });
            }

        });

    });

</script>

